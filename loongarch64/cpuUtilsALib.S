/*
 * @brief:
 *	  获取一个字的0位开始的连续0的位数（0~32）。
 * @param[in]: numVar: 32位无符号类型的变量
 * @return:
 *	  一个字的0位开始的连续0的位数。
 * @notes:
 *	  从左到右进行计数的。
 *	  如果参数为0，则返回值为32。
 */
/*T_UWORD zeroNumOfWordGet( T_UWORD numVar)*/
ENTRY(zeroNumOfWordGet)
    clz.w   a0,a0
    jirl    zero,ra,0
ENDPROC(zeroNumOfWordGet)
/*
 * @brief:
 *	  获取一个字的0位开始的连续0的位数（0~32）。
 * @param[in]: numVar: 32位无符号类型的变量
 * @return:
 *	  一个字的0位开始的连续0的位数。
 * @notes:
 *	  从左到右进行计数的。
 *	  如果参数为0，则返回值为32。
 */
/*T_UWORD zeroNumOfLongGet( T_UWORD numVar)*/
ENTRY(zeroNumOfLongGet)
    clz.d   a0,a0
    jirl    zero,ra,0
ENDPROC(zeroNumOfLongGet)
